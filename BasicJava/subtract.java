import java.util.*;
public class subtract {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.print("Enter 1st no.: ");
        int a=sc.nextInt();
        System.out.print("Enter 2nd no.: ");
        int b=sc.nextInt();
        int sub=a-b;
        System.out.print("The subract is: ");
        System.out.print(sub);
    }
}
